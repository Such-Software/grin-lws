//! View-only Grin rangeproof rewind — recognize a wallet's outputs from ONLY
//! its `rewind_hash` (a public view credential), recovering the value and the
//! derivation identifier (`key_id`) with NO spend/private key.
//!
//! This is the server-side analog of a wallet's seed-based `recover_output`,
//! but view-only. It computes the v3 rewind nonce (keyed BLAKE2b over the
//! commitment) and rewinds the Bulletproof exactly as grin-wallet's official
//! `scan_rewind_hash` does (grin/libwallet `internal/scan.rs`). Ownership is
//! decided by a successful rewind plus a well-formed 20-byte proof message.
//!
//! ## Scope + money-safety
//! - **v3 outputs only.** The legacy (pre-HF1) rewind nonce is derived from the
//!   master secret, so legacy outputs are UNDISCOVERABLE from a `rewind_hash`
//!   alone — the same limitation grin-wallet's view-only scan has. A wallet with
//!   pre-HF1 outputs will under-report here.
//! - **Advisory paths.** The recovered `key_id` / `n_child` come from the
//!   *unauthenticated* proof message; a view-only server cannot re-derive the
//!   blinding factor to confirm ownership the way a seed holder can. They are
//!   ADVISORY: a spending client MUST re-derive the commitment from its seed
//!   before using an output as an input. **`key_id` (the full 17-byte
//!   Identifier) is authoritative**; `n_child` is grin-canonical (== 0 for a
//!   Smirk `[0,0,n,0]` path — the real index lives inside `key_id`).
//! - Because we cannot run the seed-side commitment check, we apply strict
//!   message sanity checks (below) to limit view-only false positives; the
//!   bulletproof rewind itself carries a checksum, so an accidental cross-wallet
//!   match is cryptographically negligible.
//!
//! The nonce + rewind + parse logic is a faithful, minimal copy of the reusable,
//! cross-validated crypto in smirk's `grin-ext` crate (`recovery.rs`), pinned by
//! the golden-vector tests below (generated by grin's OWN reference
//! `ProofBuilder`). It depends only on the upstream published `grin_secp256k1zkp`
//! and never touches a private key.

use blake2::digest::{consts::U32, FixedOutput, Update};
use blake2::Blake2bMac;

/// Grin Pedersen commitment length (compressed): 33 bytes.
pub const COMMITMENT_LEN: usize = 33;
/// Grin output `Identifier` length: depth(1) || 4×u32(16) = 17 bytes.
pub const IDENTIFIER_LEN: usize = 17;

/// A view-only-recovered output: value + advisory derivation identifier.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Recovered {
    /// Committed value, in nanogrin.
    pub value: u64,
    /// The 17-byte Grin `Identifier`, hex-encoded (34 chars). AUTHORITATIVE — a
    /// spending client re-derives its key from this. `[0]` = depth, `[1..]` =
    /// 4×u32 big-endian path.
    pub key_id: String,
    /// grin-canonical `n_child` = `path[depth-1]`. NOTE: this is `0` for a Smirk
    /// `[0,0,n,0]` path; treat `key_id` as the source of truth for spending.
    pub n_child: u32,
}

/// v3 per-output rewind nonce: `blake2b-256(data = rewind_hash, key = commitment)`.
fn output_rewind_nonce(
    rewind_hash: &[u8; 32],
    commitment: &[u8; COMMITMENT_LEN],
) -> Result<[u8; 32], String> {
    keyed_blake2b_256(commitment, rewind_hash)
}

/// Native keyed BLAKE2b-256 (`blake2b(32, key, data)`; the key goes through
/// BLAKE2b's parameter block, NOT HMAC). Mirrors
/// `grin-ext::recovery::keyed_blake2b_256`.
fn keyed_blake2b_256(key: &[u8], data: &[u8]) -> Result<[u8; 32], String> {
    let mut mac = <Blake2bMac<U32>>::new_with_salt_and_personal(key, &[], &[])
        .map_err(|e| format!("blake2b keyed init: {e:?}"))?;
    Update::update(&mut mac, data);
    let out = mac.finalize_fixed();
    let mut res = [0u8; 32];
    res.copy_from_slice(&out);
    Ok(res)
}

/// Rewind a Bulletproof with `rewind_nonce`. On success returns the recovered
/// value + the embedded 20-byte proof message. `Ok(None)` = the nonce does not
/// match this proof (not this account's output). Mirrors
/// `grin-ext::recovery::bullet_proof_rewind_with_message`, keeping only the
/// value + message (a view-only server never needs the rewind blind).
fn bullet_proof_rewind_with_message(
    commitment: &[u8; COMMITMENT_LEN],
    rewind_nonce: &[u8; 32],
    proof_bytes: &[u8],
) -> Result<Option<(u64, Vec<u8>)>, String> {
    use secp256k1zkp::pedersen::{Commitment, RangeProof};
    use secp256k1zkp::{ContextFlag, Secp256k1, SecretKey};

    let secp = Secp256k1::with_caps(ContextFlag::Commit);
    let commit = Commitment::from_vec(commitment.to_vec());
    let nonce = SecretKey::from_slice(&secp, rewind_nonce)
        .map_err(|e| format!("invalid rewind nonce: {e:?}"))?;

    if proof_bytes.len() > secp256k1zkp::constants::MAX_PROOF_SIZE {
        return Err(format!("proof too large: {} bytes", proof_bytes.len()));
    }
    let mut proof = RangeProof {
        proof: [0u8; secp256k1zkp::constants::MAX_PROOF_SIZE],
        plen: proof_bytes.len(),
    };
    proof.proof[..proof_bytes.len()].copy_from_slice(proof_bytes);

    match secp.rewind_bullet_proof(commit, nonce, None, proof) {
        // `info.message` is the fixed 20-byte proof-message buffer; `info.mlen`
        // is unreliable (the binding reports 0), so we always take 20 bytes.
        Ok(info) => Ok(Some((info.value, info.message.as_bytes().to_vec()))),
        Err(_) => Ok(None),
    }
}

/// View-only recover: v3 nonce → rewind → parse the 20-byte message into the
/// advisory derivation identifier. There is NO commitment re-derivation (that
/// needs the seed). Returns `Ok(None)` for a non-match or an implausible message.
pub fn rewind_output_view_only(
    rewind_hash: &[u8; 32],
    commitment: &[u8; COMMITMENT_LEN],
    proof: &[u8],
) -> Result<Option<Recovered>, String> {
    let nonce = output_rewind_nonce(rewind_hash, commitment)?;
    let Some((value, message)) = bullet_proof_rewind_with_message(commitment, &nonce, proof)? else {
        return Ok(None);
    };
    Ok(parse_v3_message(value, &message))
}

/// Parse a rewound 20-byte v3 proof message into an advisory [`Recovered`].
/// Pure bytes; mirrors the v3 branch of `grin-ext::recovery::{check_output,
/// try_recover}` MINUS the private-key commitment re-derivation. Rejects
/// implausible messages (`None`) to limit view-only false positives, since the
/// seed-side ownership check is unavailable here.
fn parse_v3_message(value: u64, message: &[u8]) -> Option<Recovered> {
    // The Grin proof message is exactly 20 bytes.
    if message.len() != 20 {
        return None;
    }
    // v3 marker: reserved byte + standard wallet-type byte are both 0.
    if message[0] != 0 || message[1] != 0 {
        return None;
    }
    // Switch commitment type: 0 = None, 1 = Regular. Reject anything else.
    if message[2] > 1 {
        return None;
    }
    // Depth: grin's Identifier holds at most 4 path elements; depth 0 is not a
    // real output. (grin clamps to <=4; we reject out-of-range to compensate for
    // the missing seed-side check.)
    let depth = message[3];
    if depth == 0 || depth > 4 {
        return None;
    }
    // A zero-value output is not a real spendable output.
    if value == 0 {
        return None;
    }
    // Identifier: [0] = depth, [1..17] = the 16 path bytes verbatim.
    let mut identifier = [0u8; IDENTIFIER_LEN];
    identifier[0] = depth;
    identifier[1..IDENTIFIER_LEN].copy_from_slice(&message[4..20]);
    // grin-canonical n_child = path[depth-1] (4 big-endian u32s).
    let mut full_path = [0u32; 4];
    for (i, chunk) in message[4..20].chunks_exact(4).enumerate() {
        full_path[i] = u32::from_be_bytes([chunk[0], chunk[1], chunk[2], chunk[3]]);
    }
    let n_child = full_path[(depth as usize) - 1];
    Some(Recovered {
        value,
        key_id: hex::encode(identifier),
        n_child,
    })
}

