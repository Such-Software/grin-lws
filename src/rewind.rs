    #[test]
    fn golden_v1_depth3_grim() {
        let got = rewind_output_view_only(&arr32(RH), &arr33(V1_COMMIT), &h(V1_PROOF))
            .expect("rewind errored")
            .expect("v1 must be recovered by its own rewind_hash");
        assert_eq!(got.value, V1_VALUE, "value");
        assert_eq!(got.key_id, V1_KEY_ID, "key_id (authoritative)");
        assert_eq!(got.n_child, V1_NCHILD, "n_child canonical");
    }

    #[test]
    fn golden_v2_depth4_smirk_nchild_zero() {
        let got = rewind_output_view_only(&arr32(RH), &arr33(V2_COMMIT), &h(V2_PROOF))
            .expect("rewind errored")
            .expect("v2 must be recovered by its own rewind_hash");
        assert_eq!(got.value, V2_VALUE, "value");
        assert_eq!(got.key_id, V2_KEY_ID, "key_id (authoritative)");
        // R4: the spendable index (42) lives in key_id, NOT in n_child.
        assert_eq!(got.n_child, V2_NCHILD, "n_child is grin-canonical 0 for [0,0,n,0]");
        assert!(got.key_id.contains("2a"), "spendable index 42=0x2a must be in key_id");
    }

    #[test]
    fn wrong_rewind_hash_yields_none() {
        // Flip one byte of the correct rewind_hash → different nonce → the
        // bulletproof rewind fails → not our output.
        let mut rh = arr32(RH);
        rh[0] ^= 0x01;
        // A non-matching rewind_hash must not recover the output — whether the
        // nonce simply doesn't match (Ok(None)) or the flipped bytes aren't a
        // valid secp scalar (Err). Either way: not recovered.
        let got = rewind_output_view_only(&rh, &arr33(V1_COMMIT), &h(V1_PROOF));
        assert!(
            !matches!(got, Ok(Some(_))),
            "a non-matching rewind_hash must not recover the output: {got:?}",
        );
    }

    #[test]
    fn same_wallet_recovers_both_outputs() {
        // Both vectors are outputs of the SAME wallet, so the one rewind_hash
        // must recover both (and only value/key_id differ).
        let a = rewind_output_view_only(&arr32(RH), &arr33(V1_COMMIT), &h(V1_PROOF)).unwrap();
        let b = rewind_output_view_only(&arr32(RH), &arr33(V2_COMMIT), &h(V2_PROOF)).unwrap();
        assert!(a.is_some() && b.is_some());
        assert_ne!(a.unwrap().key_id, b.unwrap().key_id);
    }

    #[test]
    fn parse_rejects_malformed_messages() {
        // 20-byte messages that must be rejected view-only.
        assert!(parse_v3_message(100, &[0u8; 19]).is_none(), "wrong length");
        let mut m = [0u8; 20];
        m[3] = 4; // depth ok
        m[2] = 2; // invalid switch
        assert!(parse_v3_message(100, &m).is_none(), "invalid switch");
        let mut m = [0u8; 20];
        m[3] = 0; // depth 0
        assert!(parse_v3_message(100, &m).is_none(), "depth 0");
        let mut m = [0u8; 20];
        m[0] = 1; // v3 marker broken
        m[3] = 4;
        assert!(parse_v3_message(100, &m).is_none(), "bad v3 marker");
        let mut m = [0u8; 20];
        m[3] = 4;
        assert!(parse_v3_message(0, &m).is_none(), "zero value");
    }
